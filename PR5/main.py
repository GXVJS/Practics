# Ввод строки
s = input("Введите строку: ")

# Определение длины строки
n = len(s)

# Вычисление индекса середины строки (n/2)
middle = n // 2  # для целочисленного деления

# Преобразование строки
result = ""
for i in range(n):
    if i < middle and s[i] == 'п':
        result += '*'  # заменяем 'п' на '*' в первой половине
    else:
        result += s[i]  # оставляем символ без изменений

# Вывод результата
print("Результат:", result)